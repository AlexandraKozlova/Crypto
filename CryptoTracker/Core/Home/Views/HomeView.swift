import SwiftUI

struct HomeView: View {
    
    @EnvironmentObject private var vm: HomeViewModel
    @State private var showPortfolio = false // animate right
    @State private var showPortfolioView = false // new sheet
    @State private var showSettingsView = false // new sheet
    @State private var selectedCoin: Coin? = nil
    @State private var showDetailView: Bool = false
    
    var body: some View {
        ZStack {
            Color.theme.background
                .ignoresSafeArea()
                .sheet(isPresented: $showPortfolioView) {
                    PortfolioView()
                        .environmentObject(vm)
                }
            
            VStack {
                homeView
                HomeStatsView(showPortfolio: $showPortfolio)
                SearchBarView(searchingText: $vm.searchingText)
                colomnTitles
                
                if !showPortfolio {
                    allCoinsView
                        .transition(.move(edge: .trailing))
                } else {
                    ZStack(alignment: .top) {
                        if vm.portfolioCoins.isEmpty && vm.searchingText.isEmpty {
                            portfolioEmptyText
                        } else {
                            portfolioCoinsView
                        }
                    }
                    .transition(.move(edge: .leading))
                }
                Spacer(minLength: 0)
            }
            .sheet(isPresented: $showSettingsView) {
                SettingsView()
            }
        }
        .background(
            NavigationLink(isActive: $showDetailView,
                           destination: { DetailLoadingView(coin: $selectedCoin) },
                           label: { EmptyView() })
        )
    }
}

